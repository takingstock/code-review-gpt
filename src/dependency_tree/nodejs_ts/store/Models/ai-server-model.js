const mongoose = require('mongoose');

const { Schema, model } = mongoose;
const schema = new Schema({
    name: {
        type: String,
        required: true,
        unique: true
    },
    healthOcr: {
        type: String,
        default: ''
    },
    documentOcr: {
        type: String,
        required: true
    },
    documentSnipplet: {
        type: String,
        required: true
    },
    healthFeedback: {
        type: String,
        default: ''
    },
    tabularFeedback: {
        type: String,
        required: true
    },
    nonTabularFeedback: {
        type: String,
        required: true
    },
    healthDocumentBucketing: {
        type: String,
        required: ''
    },
    documentBucketing: {
        type: String,
        required: true
    },
    updateFlags: {
        type: String,
        required: true
    }
}, {
    timestamps: true,
});
module.exports = model("aiServer", schema);
